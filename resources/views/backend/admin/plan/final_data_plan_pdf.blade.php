
<div>
    <h2 style="text-align: center;">RECOMENDACIONES</h2>
    <ul style="font-size: 15px;">
        <li><strong>SAL:</strong> en el caso de utilizar sal en las comidas, preferentemente hacerlo con sal del Himalaya o sal rosa.</li>
        <li><strong>EDULCORANTES:</strong> para endulzar preparaciones o infusiones, preferentemente utilizar estevia.</li>
        <li><strong>ACEITE:</strong> de ser necesario el uso de aceite en alguna preparación se recomienda el aceite de coco.</li>
        <li><strong>CARNES:</strong> seleccionar cortes magros.</li>
        <li><strong>BEBIDAS:</strong> evitar el consumo de bebidas gaseosas o azucaradas, priorizar el consumo de agua.</li>
        <li>Masticar lentamente y disfrutar las preparaciones.</li>
        <li>Exponerse al sol, al menos 15-20 minutos, 3 veces por semana.</li>
        <li>Tener contacto con la naturaleza, pisar césped descalzo y respirar.</li>
        <li>Condimentar siempre que se pueda: pimienta negra + cúrcuma.</li>
        <li>Siempre que se coma pescado, acompañar con un poquito de cilantro crudo.</li>
        <li>Al brócoli no hervirlo más de 2 minutos, a los huevos no más de 5 minutos (se cuenta el tiempo desde el punto de ebullición).</li>
        <li>Siempre que se pueda, acompañar con 1 cucharada sopera de chucrut al día.</li>
        <li>Infusión recomendada (3 veces por semana) jengibre con canela.</li>
        <li>Las frutas son solo sugerencias, puedes elegir la que más te guste o las de temporada (ej.: ciruela en lugar de mandarina).</li>
        <li>A la banana no la uses para reemplazar otras, ni tampoco la reemplaces por otra fruta (si dice banana, es banana).</li>
    </ul>
</div>
